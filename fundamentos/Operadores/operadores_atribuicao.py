saldo = 500 #Atribuicao Simples
print(saldo)

saldo += 40 #Atribuicao Adicao
print(saldo)

saldo -= 30 #Atribuicao Subtracao
print(saldo)

saldo *= 5
print(saldo) #Atribuicao Multiplicacao

saldo /= 3 #Atribuicao Divisao
print(saldo)

saldo //= 2 #Atribuicao Divisao Inteira
print(saldo)

saldo %= 10 #Atribuicao Modulo
print(saldo)

saldo **= 5 #Atribuicao Exponenciacao
print(saldo)